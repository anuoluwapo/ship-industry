			var regex = new RegExp("(?:^|\\s+)" + className1 + "(?:\\s+|$)");
			if (regex.test(r.className)) {
				r.className = r.className.replace(regex, ' ' + className2 + ' ');
			} else {
				r.className = r.className.replace(new RegExp("(?:^|\\s+)" + className2 + "(?:\\s+|$)"), ' ' + className1 + ' ');
